public class RelationalOperator{
    public static void main(String args[]){

        int a=10;      //equal to operator
        int b=10;
        System.out.println(a==b);


        int c=10;       // not  equal to operator
        int d=10;
        System.out.println(c!=d);

        int e=10;      //rgreater than operator
        int f=5;
        System.out.println(e>f);

        int h=9;        //less than operator
        int i=10;
        System.out.println(h<i);


        int j=10;         //rgreater than equal to operator
        int k=10;
        System.out.println(j>=k);

        int m=10;            //less than equal to operator
        int n=10;
        System.out.println(m<=n);
    }
}